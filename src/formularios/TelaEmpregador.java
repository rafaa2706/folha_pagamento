    private void calcularHorasExtras() throws ParseException {
        
         // Define o formato numérico para a localidade Brasil
            NumberFormat format = NumberFormat.getInstance(new Locale("pt", "BR"));
            
            // Converte a string para número
            Number numero = format.parse(jLabelSalarioBruto.getText());
            
            // Converte para double
            double salarioBruto = numero.doubleValue();
        
      
    
    // Obtenha o valor das horas trabalhadas do JLabel e converta para int
     
    int horasTrabalhadas = Integer.parseInt(jLabelHorasTrabalhadas.getText().replace("h", ""));

    // Calcular o valor da hora normal
    double valorHoraNormal = salarioBruto / horasTrabalhadas;

    // Obtenha o valor das horas extras e feriados dos JTextFields e converta para int
    int horasExtras = Integer.parseInt(jTextFieldHoraExtra.getText());
    int horasFeriado = Integer.parseInt(jTextFieldHefd.getText());

    // Cálculo das horas extras e feriados
    double valorHorasExtras = horasExtras * valorHoraNormal * 1.5; // 50% a mais para horas extras
    double valorHorasFeriado = horasFeriado * valorHoraNormal * 2.0; // 100% a mais para domingos e feriados

    // Exibir apenas os valores numéricos no JLabel
    jLabelHE.setText(String.format("%.2f", valorHorasExtras));
    jLabelHEFD.setText(String.format("%.2f", valorHorasFeriado));
    
    }
   
        
