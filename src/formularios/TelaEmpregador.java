    private void calcularFaltas() {
    // Obter a quantidade de faltas do jTextFieldFaltas
    int quantidadeFaltas = Integer.parseInt(jTextFieldFaltas.getText());

    // Calcular o valor de um dia de trabalho (salário bruto dividido por 30 dias)
    double valorDia = salarioBruto / 30;

    // Calcular o valor total das faltas
    double valorFaltas = valorDia * quantidadeFaltas;

    // Exibir o valor das faltas no jLabelValorFaltas, formatado como moeda
    DecimalFormat df = new DecimalFormat("##,##0.00");
    jLabelValorFaltas.setText(df.format(valorFaltas));
    }
    
    private void calcularSalarioLiquido() {
    // Obter o salário bruto do JLabel (removendo "R$" e formatando corretamente)
    // Obter os descontos dos JLabel (INSS, Vale Transporte, IRRF e Faltas, Vencimentos)
    double inss = this.formatarDecimal(jLabelINSS.getText());
    double valeTransporte = this.formatarDecimal(jLabelValeTransporte.getText());
    double irrf = this.formatarDecimal(jLabelIRRF.getText());
    
    double valorFaltas = this.formatarDecimal(jLabelValorFaltas.getText());
    double vencimentos = this.formatarDecimal(jLabelVencimentos.getText());
    double auxilio = this.formatarDecimal(jLabelAuxilio.getText());
    // Somar todos os descontos
    double descontos = inss + valeTransporte + irrf + valorFaltas;
    
    // Calcular o salário líquido
    double salarioLiquido = salarioBruto - descontos + vencimentos + auxilio ;

    // Exibir o resultado no JLabel de vencimentos
    jLabelSalLiquido.setText(String.format("%.2f", salarioLiquido));
    // Exibir o resultado no JLabel de descontos
    jLabelDescontos.setText( String.format("%.2f", descontos));
    }

    

    
    private double calcularAuxilio(double salario, int quantidadeDependentes) {
        double salarioLimite = 1819.26;
        double valorAuxilioPorDependente = 62.04;

        
        if (salario <= salarioLimite) {
            return quantidadeDependentes * valorAuxilioPorDependente;
        } else {
            return 0; // Sem direito ao auxílio se o salário for maior que o limite
        }
    }
