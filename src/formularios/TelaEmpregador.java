    private void jButtonCalcularDescontosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCalcularDescontosActionPerformed
        try {
            double salario = salarioBruto;
            int dependentes = qtdDependValIR;  // Obtenha o número de dependentes do método

            // Cálculo do INSS
            double aliquotaINSS = 0;
            double parcelaDeduzirINSS = 0;

            if (salario <= 1412.00) {
                aliquotaINSS = 0.075;
                parcelaDeduzirINSS = 0;
            } else if (salario <= 2666.68) {
                aliquotaINSS = 0.09;
                parcelaDeduzirINSS = 21.18;
            } else if (salario <= 4000.03) {
                aliquotaINSS = 0.12;
                parcelaDeduzirINSS = 101.18;
            } else if (salario <= 7786.02) {
                aliquotaINSS = 0.14;
                parcelaDeduzirINSS = 181.18;
            }

            double descontoINSS = (salario * aliquotaINSS) - parcelaDeduzirINSS;

            // Cálculo do Vale Transporte - Verificar se o colaborador optou por receber o vale
            double descontoValeTransporte = 0;
            if (jCheckBoxValeTransporte.isSelected()) {  // Se o checkbox estiver marcado
                descontoValeTransporte = salario * 0.06; // Aplica o desconto de 6%
            }

            // Cálculo da Base de Cálculo do IRRF
            double deducaoDependentes = dependentes * 189.59;
            double baseCalculoIRRF = salario - descontoINSS - deducaoDependentes;

            // Cálculo do IRRF
            double aliquotaIRRF = 0;
            double parcelaDeduzirIRRF = 0;

            if (baseCalculoIRRF <= 2259.20) {
                aliquotaIRRF = 0;
                parcelaDeduzirIRRF = 0;
            } else if (baseCalculoIRRF <= 2826.65) {
                aliquotaIRRF = 0.075;
                parcelaDeduzirIRRF = 169.44;
            } else if (baseCalculoIRRF <= 3751.05) {
                aliquotaIRRF = 0.15;
                parcelaDeduzirIRRF = 381.44;
            } else if (baseCalculoIRRF <= 4664.68) {
                aliquotaIRRF = 0.225;
                parcelaDeduzirIRRF = 662.77;
            } else {
                aliquotaIRRF = 0.275;
                parcelaDeduzirIRRF = 896.00;
            }

            double descontoIRRF = (baseCalculoIRRF * aliquotaIRRF) - parcelaDeduzirIRRF;

            // Formatar o valor para exibir no JLabel
            DecimalFormat df = new DecimalFormat("##,##0.00");
            jLabelINSS.setText( df.format(descontoINSS));
            jLabelIRRF.setText( df.format(descontoIRRF));
            jLabelValeTransporte.setText( df.format(descontoValeTransporte));

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Por favor, insira um valor de salário válido.");
        }

        calcularFaltas();

        calcularSalarioLiquido();
    }//GEN-LAST:event_jButtonCalcularDescontosActionPerformed

